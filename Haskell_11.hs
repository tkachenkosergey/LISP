Реализовать на языке Haskell функцию, которая, чередует элементы списков [a, b, ...] и [1, 2, ...],
образует новый список [a, 1, b, 2, ...]

f :: [Integer] -> [Integer] -> [Integer] 
f [a] [b] = [a] ++ [b] 
f (a:t) (b:t1) = [a] ++ [b] ++ f t t1

main = print $ f [ 7 , 7 , 7 , 7 , 7]  [1 , 2 , 3 , 4 , 5] -> [7,1,7,2,7,3,7,4,7,5]
